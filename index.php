// Script adicional para reinicializar Isotope después de cargar contenido dinámico
document.addEventListener('DOMContentLoaded', function() {
    // Esperar a que las imágenes se carguen completamente
    const images = document.querySelectorAll('.portfolio-item img');
    let loadedImages = 0;
    
    function checkAllImagesLoaded() {
        loadedImages++;
        if (loadedImages === images.length && images.length > 0) {
            // Reinicializar Isotope cuando todas las imágenes estén cargadas
            if (typeof Isotope !== 'undefined') {
                const grid = document.querySelector('.isotope-container');
                if (grid) {
                    new Isotope(grid, {
                        itemSelector: '.portfolio-item',
                        layoutMode: 'masonry'
                    });
                }
            }
        }
    }
    
    if (images.length === 0) {
        // Si no hay imágenes, inicializar Isotope directamente
        if (typeof Isotope !== 'undefined') {
            const grid = document.querySelector('.isotope-container');
            if (grid) {
                new Isotope(grid, {
                    itemSelector: '.portfolio-item',
                    layoutMode: 'masonry'
                });
            }
        }
    } else {
        images.forEach(img => {
            if (img.complete) {
                checkAllImagesLoaded();
            } else {
                img.addEventListener('load', checkAllImagesLoaded);
                img.addEventListener('error', checkAllImagesLoaded); // También contar errores
            }
        });
    }
});
